१६. यानीध भूतानि समागतानि, भुम्मानि वा यानि व अन्तलिक्खे।
तथागतं देवमनुस्सपूजितं, बुद्धं नमस्साम सुवत्थि होतु॥

१७. यानीध भूतानि समागतानि, भुम्मानि वा यानि व अन्तलिक्खे।
तथागतं देवमनुस्सपूजितं, धम्मं नमस्साम सुवत्थि होतु॥

१८. यानीध भूतानि समागतानि, भुम्मानि वा यानि व अन्तलिक्खे।
तथागतं देवमनुस्सपूजितं, सङ्घं नमस्साम सुवत्थि होतूति॥
रतनसुत्तं निट्ठितं।

<b> ७. तिरोकुट्टसुत्तं </b>

१. तिरोकुट्टेसु तिट्ठन्ति, सन्धिसिङ्घाटकेसु च।
द्वारबाहासु तिट्ठन्ति, आगन्त्वान सकं घरं॥

२. पहूते अन्‍नपानम्हि, खज्‍जभोज्‍जे उपट्ठिते।
न तेसं कोचि सरति, सत्तानं कम्मपच्‍चया॥

३. एवं ददन्ति ञातीनं, ये होन्ति अनुकम्पका।
सुचिं पणीतं कालेन, कप्पियं पानभोजनं।
इदं वो ञातीनं होतु, सुखिता होन्तु ञातयो॥

४. ते च तत्थ समागन्त्वा, ञातिपेता समागता।
पहूते अन्‍नपानम्हि, सक्‍कच्‍चं अनुमोदरे॥

५. चिरं जीवन्तु नो ञाती, येसं हेतु लभामसे।
अम्हाकञ्‍च कता पूजा, दायका च अनिप्फला॥

६. न हि तत्थ कसि अत्थि, गोरक्खेत्थ न विज्‍जति।
वणिज्‍जा तादिसी नत्थि, हिरञ्‍ञेन कयोकयं।
इतो दिन्‍नेन यापेन्ति, पेता कालङ्कता तहिं॥

७. उन्‍नमे उदकं वुट्ठं, यथा निन्‍नं पवत्तति।
एवमेव इतो दिन्‍नं, पेतानं उपकप्पति॥

८. यथा वारिवहा पूरा, परिपूरेन्ति सागरं।
एवमेव इतो दिन्‍नं, पेतानं उपकप्पति॥

९. अदासि मे अकासि मे, ञातिमित्ता सखा च मे।
पेतानं दक्खिणं दज्‍जा, पुब्बे कतमनुस्सरं॥

१०. न हि रुण्णं वा सोको वा, या चञ्‍ञा परिदेवना।
न तं पेतानमत्थाय, एवं तिट्ठन्ति ञातयो॥

११. अयञ्‍च खो दक्खिणा दिन्‍ना, सङ्घम्हि सुप्पतिट्ठिता।
दीघरत्तं हितायस्स, ठानसो उपकप्पति॥

१२. सो ञातिधम्मो च अयं निदस्सितो, पेतान पूजा च कता उळारा।
बलञ्‍च भिक्खूनमनुप्पदिन्‍नं , तुम्हेहि पुञ्‍ञं पसुतं अनप्पकन्ति॥

तिरोकुट्टसुत्तं निट्ठितं।

<b>८. निधिकण्डसुत्तं </b>

१. निधिं निधेति पुरिसो, गम्भीरे ओदकन्तिके।
अत्थे किच्‍चे समुप्पन्‍ने, अत्थाय मे भविस्सति॥

२. राजतो वा दुरुत्तस्स, चोरतो पीळितस्स वा।
इणस्स वा पमोक्खाय, दुब्भिक्खे आपदासु वा।
एतदत्थाय लोकस्मिं, निधि नाम निधीयति॥

३. तावस्सुनिहितो सन्तो, गम्भीरे ओदकन्तिके।
न सब्बो सब्बदा एव, तस्स तं उपकप्पति॥

४. निधि वा ठाना चवति, सञ्‍ञा वास्स विमुय्हति।
नागा वा अपनामेन्ति, यक्खा वापि हरन्ति नं॥

५. अप्पिया वापि दायादा, उद्धरन्ति अपस्सतो।
यदा पुञ्‍ञक्खयो होति, सब्बमेतं विनस्सति॥

६. यस्स दानेन सीलेन, संयमेन दमेन च।
निधी सुनिहितो होति, इत्थिया पुरिसस्स वा॥

७. चेतियम्हि च सङ्घे वा, पुग्गले अतिथीसु वा।
मातरि पितरि चापि , अथो जेट्ठम्हि भातरि॥

८. एसो निधि सुनिहितो, अजेय्यो अनुगामिको।
पहाय गमनीयेसु, एतं आदाय गच्छति॥

९. असाधारणमञ्‍ञेसं, अचोराहरणो निधि।
कयिराथ धीरो पुञ्‍ञानि, यो निधि अनुगामिको॥

१०. एस देवमनुस्सानं, सब्बकामददो निधि।
यं यदेवाभिपत्थेन्ति, सब्बमेतेन लब्भति॥

११. सुवण्णता सुसरता, सुसण्ठाना सुरूपता।
आधिपच्‍चपरिवारो, सब्बमेतेन लब्भति॥

१२. पदेसरज्‍जं इस्सरियं, चक्‍कवत्तिसुखं पियं।
देवरज्‍जम्पि दिब्बेसु, सब्बमेतेन लब्भति॥

१३. मानुस्सिका च सम्पत्ति, देवलोके च या रति।
या च निब्बानसम्पत्ति, सब्बमेतेन लब्भति॥

१४. मित्तसम्पदमागम्म, योनिसोव पयुञ्‍जतो।
विज्‍जा विमुत्ति वसीभावो, सब्बमेतेन लब्भति॥

१५. पटिसम्भिदा विमोक्खा च, या च सावकपारमी।
पच्‍चेकबोधि बुद्धभूमि, सब्बमेतेन लब्भति॥

१६. एवं महत्थिका एसा, यदिदं पुञ्‍ञसम्पदा।
तस्मा धीरा पसंसन्ति, पण्डिता कतपुञ्‍ञतन्ति॥

निधिकण्डसुत्तं निट्ठितं।

<b> ९. मेत्तसुत्तं </b>

१. करणीयमत्थकुसलेन, यन्तसन्तं पदं अभिसमेच्‍च।
सक्‍को उजू च सुहुजू च, सुवचो चस्स मुदु अनतिमानी॥

२. सन्तुस्सको च सुभरो च, अप्पकिच्‍चो च सल्‍लहुकवुत्ति।
सन्तिन्द्रियो च निपको च, अप्पगब्भो कुलेस्वननुगिद्धो॥

३. न च खुद्दमाचरे किञ्‍चि, येन विञ्‍ञू परे उपवदेय्युं।
सुखिनोव खेमिनो होन्तु, सब्बसत्ता भवन्तु सुखितत्ता॥

४. ये केचि पाणभूतत्थि, तसा वा थावरा वनवसेसा।
दीघा वा येव महन्ता , मज्झिमा रस्सका अणुकथूला॥

५. दिट्ठा वा येव अदिट्ठा दूरे वसन्ति अविदूरे।
भूता व , सब्बसत्ता भवन्तु सुखितत्ता॥

६. न परो परं निकुब्बेथ, नातिमञ्‍ञेथ कत्थचि न कञ्‍चि।
ब्यारोसना पटिघसञ्‍ञा, नाञ्‍ञमञ्‍ञस्स दुक्खमिच्छेय्य॥

७. माता यथा नियं पुत्तमायुसा एकपुत्तमनुरक्खे।
एवम्पि सब्बभूतेसु, मानसं भावये अपरिमाणं॥

८. मेत्तञ्‍च सब्बलोकस्मि, मानसं भावये अपरिमाणं।
उद्धं अधो च तिरियञ्‍च, असम्बाधं अवेरमसपत्तं॥

९. तिट्ठं चरं निसिन्‍नो व, सयानो यावतास्स वितमिद्धो।
एतं सतिं अधिट्ठेय्य, ब्रह्ममेतं विहारमिधमाहु॥

१०. दिट्ठिञ्‍च अनुपग्गम्म, सीलवा दस्सनेन सम्पन्‍नो।
कामेसु विनय गेधं, न हि जातुग्गब्भसेय्य पुन रेतीति॥

मेत्तसुत्तं निट्ठितं।

खुद्दकपाठपाळि निट्ठिता।
